    def extract_camera_from_video(self, video_path, use_cache=True):
        """
        Extract camera poses from a reference video using TTT3R.
        Uses cached poses if available.
        """
        if video_path is None:
            return "❌ No video selected", ""
        
        print(f"🎥 Processing camera poses from: {video_path}")
        
        try:
            # Try to load from cache first
            if use_cache:
                cached_poses, cached_intrinsics = load_cached_poses(video_path)
                if cached_poses is not None:
                    self.extracted_global_poses = cached_poses.to(self.generator.device)
                    self.extracted_global_intrinsics = cached_intrinsics.to(self.generator.device)
                    self.reference_video_path = video_path
                    self.extraction_mode_active = True
                    self.extracted_pose_frame_index = 0
                    
                    num_frames = cached_poses.shape[1]
                    n_frames_per_chunk = self.config.inference_params.n_frames
                    ctx = self.config.inference_params.context_frames_2nd
                    
                    if num_frames <= n_frames_per_chunk:
                        max_chunks = 1
                    else:
                        max_chunks = 1 + (num_frames - n_frames_per_chunk) // (n_frames_per_chunk - ctx)
                    
                    status_msg = f"✅ Loaded cached camera poses!\n"
                    status_msg += f"📊 Total frames: {num_frames}\n"
                    status_msg += f"📹 Max chunks (without extension): ~{max_chunks}"
                    
                    info_msg = f"Reference: {os.path.basename(video_path)}\n"
                    info_msg += f"Frames: {num_frames} | Max chunks: {max_chunks}\n"
                    info_msg += f"💡 Using cached poses!"
                    
                    return status_msg, info_msg
            
            # Extract poses using TTT3R
            print(f"🔄 Extracting poses (no cache found)...")
            
            # Preprocess video
            full_vid = preprocess_video_from_path(
                video_path, 
                self.config.inference_params.height, 
                self.config.inference_params.width, 
                600  # Max frames
            ).unsqueeze(0).to(self.generator.device)
            
            print(f"📹 Video preprocessed: {full_vid.shape[1]} frames")
            
            # Extract poses using TTT3R
            with torch.no_grad():
                _, global_poses, global_intrinsics = self.generator.ttt3r.inference(full_vid)
            
            # Fix intrinsics principal point
            global_intrinsics[:, :, 0, -1] = self.config.inference_params.width / 2.0
            global_intrinsics[:, :, 1, -1] = self.config.inference_params.height / 2.0
            global_intrinsics = global_intrinsics.mean(dim=1, keepdim=True).repeat(
                1, global_poses.shape[1], 1, 1
            )
            
            # Save to cache
            save_poses_to_cache(video_path, global_poses, global_intrinsics)
            
            # Store extracted poses
            self.extracted_global_poses = global_poses.to(self.generator.device)
            self.extracted_global_intrinsics = global_intrinsics.to(self.generator.device)
            self.reference_video_path = video_path
            self.extraction_mode_active = True
            self.extracted_pose_frame_index = 0
            
            num_frames = global_poses.shape[1]
            
            # Calculate how many chunks can be generated without extension
            n_frames_per_chunk = self.config.inference_params.n_frames
            ctx = self.config.inference_params.context_frames_2nd
            
            if num_frames <= n_frames_per_chunk:
                max_chunks = 1
            else:
                max_chunks = 1 + (num_frames - n_frames_per_chunk) // (n_frames_per_chunk - ctx)
            
            # Visualize trajectory
            traj_path = os.path.join(self.generator.pm.dirs["visualizations"], "extracted_camera_trajectory.png")
            self.generator.pose_controller.visualize_trajectory(global_poses, traj_path)
            
            status_msg = f"✅ Camera poses extracted & cached!\n"
            status_msg += f"📊 Total frames: {num_frames}\n"
            status_msg += f"📹 Max chunks (without extension): ~{max_chunks}\n"
            status_msg += f"💾 Trajectory saved to: {traj_path}"
            
            info_msg = f"Reference: {os.path.basename(video_path)}\n"
            info_msg += f"Frames: {num_frames} | Max chunks: {max_chunks}\n"
            info_msg += f"💡 Poses cached for future use!"
            
            print(status_msg)
            return status_msg, info_msg
            
        except Exception as e:
            error_msg = f"❌ Failed to extract camera poses: {str(e)}"
            print(error_msg)
            import traceback
            traceback.print_exc()
            return error_msg, ""
    
    def clear_extracted_poses(self):
        """Clear the extracted camera poses and return to normal mode"""
        self.extracted_global_poses = None
        self.extracted_global_intrinsics = None
        self.reference_video_path = None
        self.extraction_mode_active = False
        self.extracted_pose_frame_index = 0
        return "✅ Extracted poses cleared. Select a new video.", ""
    
    def _extend_poses_if_needed(self, global_poses, global_intrinsics, total_frames_needed, speed_multiplier=1.0):
        """
        Extend poses if needed, using the selected extension mode.
        Also handles speed adjustment via interpolation.
        """
        current_frames = global_poses.shape[1]

        if speed_multiplier != 1.0:
            if speed_multiplier < 1.0:
                # Slow down: interpolate to more frames
                target_frames = int(current_frames / speed_multiplier)
                print(f"⏱️ Slowing down {speed_multiplier:.2f}x: {current_frames} → {target_frames} frames")
            else:
                # Speed up: subsample to fewer frames
                target_frames = int(current_frames / speed_multiplier)
                print(f"⏱️ Speeding up {speed_multiplier:.2f}x: {current_frames} → {target_frames} frames")
            
            global_poses, global_intrinsics = extend_poses_interpolate_stretch(
                global_poses, global_intrinsics, target_frames
            )
            current_frames = target_frames
        
        # Then extend if still not enough
        if total_frames_needed > current_frames:
            n_new_frames = total_frames_needed - current_frames + 10  # Add buffer
            
            mode = self.pose_extension_mode
            print(f"📐 Need {total_frames_needed} frames, have {current_frames}. Extending with mode: {mode}")
            
            if mode == "extrapolate":
                global_poses, global_intrinsics = extend_poses_extrapolate(
                    global_poses, global_intrinsics, n_new_frames
                )
            elif mode == "loop":
                global_poses, global_intrinsics = extend_poses_loop(
                    global_poses, global_intrinsics, n_new_frames
                )
            elif mode == "pingpong":
                global_poses, global_intrinsics = extend_poses_pingpong(
                    global_poses, global_intrinsics, n_new_frames
                )
            elif mode == "slowdown":
                global_poses, global_intrinsics = extend_poses_slowdown(
                    global_poses, global_intrinsics, n_new_frames
                )
            else:
                # Default to extrapolate
                global_poses, global_intrinsics = extend_poses_extrapolate(
                    global_poses, global_intrinsics, n_new_frames
                )
        
        return global_poses, global_intrinsics
    
    def _calculate_chunk_pose_end_frame(self, chunk_idx, context_frames_2nd):
        """
        Calculate the end frame index for poses needed by a chunk.
        
        The poses always start from frame 0 (full history needed).
        We only need to calculate the end frame.
        
        For chunk i:
        - Chunk 0: end = n_frames
        - Chunk i (i >= 1): end = n_frames + i * (n_frames - ctx)
        """
        n_frames = self.config.inference_params.n_frames
        
        if chunk_idx == 0:
            end_frame = n_frames
        else:
            end_frame = n_frames + chunk_idx * (n_frames - context_frames_2nd)
        
        return end_frame
    
    def generate_chunks_from_extracted(self, prompt, negative_prompt, num_chunks,
                                       context_frames_2nd, strength, num_gs_iterations, seed,
                                       extension_mode, speed_multiplier):
        """
        Generate chunks using the extracted camera poses from a reference video.
        """
        if self.starting_image is None:
            return None, "❌ Please set a starting image first (generate or upload)", "", ""
        
        if not self.extraction_mode_active or self.extracted_global_poses is None:
            return None, "❌ Please select and load a reference video first", "", ""
        
        # Update extension mode
        self.pose_extension_mode = extension_mode
        
        print(f"🎬 Generating {num_chunks} chunk(s) using extracted camera poses...")
        print(f"📐 Extension mode: {POSE_EXTENSION_MODES[extension_mode][0]}")
        print(f"⏱️ Speed multiplier: {speed_multiplier}x")
        
        # Update config
        self.config.video_source.prompt = prompt
        self.config.prompts.positive = prompt
        self.config.prompts.negative = negative_prompt
        self.config.inference_params.context_frames_2nd = context_frames_2nd
        self.config.inference_params.minxs = 1.0 - strength
        self.config.inference_params.num_gs_iterations = num_gs_iterations
        self.config.experiment.seed = seed
        
        # Set seed
        seed_everything(seed)
        
        # Calculate the maximum frame index needed (for the last chunk)
        last_chunk_idx = len(self.video_chunks) + num_chunks - 1
        total_frames_needed = self._calculate_chunk_pose_end_frame(last_chunk_idx, context_frames_2nd)
        
        # Get extended poses (apply speed multiplier and extension)
        global_poses = self.extracted_global_poses.clone()
        global_intrinsics = self.extracted_global_intrinsics.clone()
        
        global_poses, global_intrinsics = self._extend_poses_if_needed(
            global_poses, global_intrinsics, total_frames_needed, speed_multiplier
        )
        global_poses = global_poses.to(self.generator.device)
        global_intrinsics = global_intrinsics.to(self.generator.device)
        
        print(f"📊 Total frames available after extension: {global_poses.shape[1]}")
        print(f"📊 Total frames needed: {total_frames_needed}")
        
        # Generate requested number of chunks
        for chunk_iter in range(num_chunks):
            chunk_idx = len(self.video_chunks)
            is_first = (chunk_idx == 0)
            ctx = self.config.inference_params.context_frames if is_first else context_frames_2nd
            n_frames_per_chunk = self.config.inference_params.n_frames
            
            # Store context frames for stitching
            self.chunk_ctx_log.append(ctx)
            
            # Calculate end frame for this chunk (poses always start from 0)
            end_frame = self._calculate_chunk_pose_end_frame(chunk_idx, context_frames_2nd)
            
            # Ensure we don't exceed available frames
            if end_frame > global_poses.shape[1]:
                print(f"⚠️ Warning: end_frame {end_frame} exceeds available {global_poses.shape[1]}, clamping")
                end_frame = global_poses.shape[1]
            
            print(f"📍 Chunk {chunk_idx}: Using poses [0:{end_frame}] (ctx={ctx})")
            
            # Track movement type
            self.chunk_movements.append([f"EXTRACTED ({extension_mode})"])
            
            # Update loop params
            self.config.loop_params.n_chunks = len(self.chunk_movements)
            
            # Determine input video
            if chunk_idx == 0:
                input_video = self._image_to_video(self.starting_image)
            else:
                input_video = self.video_chunks[-1]
            
            start_frame = max((n_frames_per_chunk - ctx) * (chunk_idx - 1), 0)
            end_frame = start_frame + n_frames_per_chunk * 2 - ctx
            chunk_poses = global_poses[:, start_frame:end_frame].clone()
            chunk_intrinsics = global_intrinsics[:, start_frame:end_frame].clone()
            # Generate chunk
            print(f"📹 Generating chunk {chunk_idx + 1}...")
            chunk_path = self.generator.run_inference_chunk(
                chunk_idx, input_video, chunk_poses, chunk_intrinsics,
                ctx, is_first, style_prompt=prompt, combined_video_path=self.combined_video_path
            )
            
            # Read caption generated by run_inference_chunk
            caption_file = os.path.join(self.generator.pm.dirs["captions"], f"chunk_{chunk_idx:03d}.txt")
            if os.path.exists(caption_file):
                with open(caption_file, "r") as f:
                    captioned_prompt = f.read()
                self.chunk_captions.append(captioned_prompt)
            else:
                self.chunk_captions.append(prompt)
            
            self.video_chunks.append(chunk_path)
            
            # Stitch all chunks
            self.combined_video_path = self._stitch_all()
        
        history = self._format_history()
        captions_display = self._format_captions()
        
        return self.combined_video_path, f"✅ Generated {num_chunks} chunk(s) from extracted poses", history, captions_display

