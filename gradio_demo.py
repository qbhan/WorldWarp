EXAMPLE_VIDEOS_DIR = "examples/videos"  # Directory containing example videos
POSE_CACHE_DIR = "examples/pose_cache"  # Directory to cache extracted poses
EXAMPLE_IMAGES_DIR = "examples/images"  # Directory containing example starting images


def get_example_videos():
    """Get list of example videos from the examples directory"""
    if not os.path.exists(EXAMPLE_VIDEOS_DIR):
        os.makedirs(EXAMPLE_VIDEOS_DIR, exist_ok=True)
        return []
    
    video_extensions = ['*.mp4', '*.avi', '*.mov', '*.mkv', '*.webm']
    videos = []
    for ext in video_extensions:
        videos.extend(glob.glob(os.path.join(EXAMPLE_VIDEOS_DIR, ext)))
    
    return sorted(videos)


def get_example_images():
    """
    Get example images from examples/images/ directory.
    Images can have .jpg, .jpeg, or .png extension.
    Prompts will be auto-generated by Qwen captioner when needed.
    
    Returns:
        list of tuples: [(image_path, name), ...]
    """
    examples = []
    
    if not os.path.exists(EXAMPLE_IMAGES_DIR):
        os.makedirs(EXAMPLE_IMAGES_DIR, exist_ok=True)
        return examples
    
    # Get all images
    image_extensions = ['*.jpg', '*.jpeg', '*.png']
    image_files = []
    for ext in image_extensions:
        image_files.extend(glob.glob(os.path.join(EXAMPLE_IMAGES_DIR, ext)))
    
    # Return image path and name (no prompts - Qwen will caption)
    for img_path in sorted(image_files):
        img_name = os.path.splitext(os.path.basename(img_path))[0]
        examples.append((img_path, img_name))
    
    return examples


def get_pose_cache_path(video_path):
    """Get the cache file path for a video's poses"""
    os.makedirs(POSE_CACHE_DIR, exist_ok=True)
    video_name = os.path.splitext(os.path.basename(video_path))[0]
    return os.path.join(POSE_CACHE_DIR, f"{video_name}_poses.npz")


def load_cached_poses(video_path):
    """Load cached poses if they exist"""
    cache_path = get_pose_cache_path(video_path)
    if os.path.exists(cache_path):
        print(f"📂 Loading cached poses from: {cache_path}")
        data = np.load(cache_path)
        poses = torch.from_numpy(data['poses'])
        intrinsics = torch.from_numpy(data['intrinsics'])
        return poses, intrinsics
    return None, None


def save_poses_to_cache(video_path, poses, intrinsics):
    """Save extracted poses to cache"""
    cache_path = get_pose_cache_path(video_path)
    print(f"💾 Saving poses to cache: {cache_path}")
    np.savez(cache_path, 
             poses=poses.cpu().numpy(), 
             intrinsics=intrinsics.cpu().numpy())


