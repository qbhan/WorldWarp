        print("🎨 Generating first frame from prompt...")
        
        # Always ensure text-only captioner for T2V generation
        print("🔄 Initializing text-only captioner for T2V...")
        self.generator._init_captioner(is_vl=False)
        
        # Update config for first frame generation
        temp_config = OmegaConf.to_container(self.config, resolve=True)
        temp_config = OmegaConf.create(temp_config)
        temp_config.video_source.mode = "t2v"
        temp_config.video_source.prompt = prompt
        temp_config.video_source.t2v_length = 49  # Generate 49 frames
        temp_config.prompts.negative = negative_prompt
        
        # Temporarily update generator config
        old_config = self.generator.cfg
        self.generator.cfg = temp_config
        
        # Generate video
        video_path = self.generator.prepare_source_video()
        
        # Extract first frame
        reader = imageio.get_reader(video_path)
