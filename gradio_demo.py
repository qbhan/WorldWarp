    def _image_to_video(self, image_path, num_frames=None):
        """Convert image to video for first chunk"""
        img = Image.open(image_path)
        img = img.resize((self.config.inference_params.width, 
                         self.config.inference_params.height))
        frame = np.array(img)
        
        if num_frames is None:
            num_frames = self.config.inference_params.n_frames
        
        frames = [frame] * num_frames
        
        output_path = os.path.join(self.temp_dir, f"starting_video_{num_frames}f.mp4")
        imageio.mimsave(output_path, frames, fps=self.config.loop_params.output_fps)
        return output_path
    
    def _stitch_all(self):
        """Stitch all generated chunks"""
        if len(self.video_chunks) == 0:
            return None
        
        out_path = os.path.join(self.temp_dir, f"combined_{len(self.video_chunks)}.mp4")
        
        print(f"🎞️ Stitching {len(self.video_chunks)} chunks...")
        print(f"📊 chunk_ctx_log: {self.chunk_ctx_log}")
        
        total_frames = 0
        with imageio.get_writer(out_path, fps=self.config.loop_params.output_fps) as writer:
            for i, path in enumerate(self.video_chunks):
                reader = imageio.get_reader(path)
                chunk_frame_count = reader.count_frames()
                # Use stored context frames for this chunk (first chunk skips 0)
                skip = 0 if i == 0 else self.chunk_ctx_log[i]
                frames_written = 0
                for k, frame in enumerate(reader):
                    if k >= skip:
                        writer.append_data(frame)
                        frames_written += 1
                reader.close()
                print(f"  Chunk {i}: {chunk_frame_count} frames, skip={skip}, written={frames_written}")
                total_frames += frames_written
        
        print(f"✅ Combined video: {total_frames} total frames -> {out_path}")
        return out_path
    
    def _format_history(self):
        """Format generation history"""
        if len(self.chunk_movements) == 0:
            return "No chunks generated yet"
        
        history = []
        for i, movements in enumerate(self.chunk_movements):
            if movements == ["CUSTOM"]:
                history.append(f"Chunk {i+1}: CUSTOM")
            elif movements[0].startswith("EXTRACTED"):
                history.append(f"Chunk {i+1}: {movements[0]}")
            else:
                history.append(f"Chunk {i+1}: {' + '.join(movements)}")
        return "\n".join(history)
    
    def _format_captions(self):
        """Format captioned prompts for display"""
        if len(self.chunk_captions) == 0:
            return "No captions yet"
        
        captions = []
        for i, caption in enumerate(self.chunk_captions):
            captions.append(f"=== Chunk {i+1} ===")
            captions.append(caption)
            captions.append("")
        return "\n".join(captions)
    
    def reset(self):
        """Reset all state"""
        print("🔄 Resetting session...")
        self.starting_image = None
        self.video_chunks = []
        self.chunk_movements = []
        self.chunk_captions = []
        self.chunk_ctx_log = []  # Clear context log
        self.combined_video_path = None
        self.config.camera_pose_control.chunk_poses = []
        
        self.extracted_global_poses = None
        self.extracted_global_intrinsics = None
        self.reference_video_path = None
        self.extraction_mode_active = False
        self.pose_extension_mode = "extrapolate"
        self.extracted_pose_frame_index = 0
        
        print("🔄 Resetting captioner to text-only mode...")
        self.generator._init_captioner(is_vl=False)
        
        return None, None, "Reset complete", "", ""
    
    def rollback_to_chunk(self, chunk_number):
        """Rollback to a specific chunk, deleting all chunks after it"""
        if chunk_number < 1:
            return None, "❌ Invalid chunk number", "", ""
        
        if chunk_number > len(self.video_chunks):
            return self.combined_video_path, f"❌ Only {len(self.video_chunks)} chunks exist", self._format_history(), self._format_captions()
        
        self.video_chunks = self.video_chunks[:chunk_number]
        self.chunk_movements = self.chunk_movements[:chunk_number]
        self.chunk_captions = self.chunk_captions[:chunk_number]
        self.chunk_ctx_log = self.chunk_ctx_log[:chunk_number]  # Also rollback ctx_log
        self.config.camera_pose_control.chunk_poses = self.config.camera_pose_control.chunk_poses[:chunk_number]
        
        if len(self.video_chunks) > 0:
            self.combined_video_path = self._stitch_all()
        else:
            self.combined_video_path = None
        
        history = self._format_history()
        captions = self._format_captions()
        
        print(f"✂️ Rolled back to chunk {chunk_number}")
        return self.combined_video_path, f"✂️ Rolled back to chunk {chunk_number}", history, captions


