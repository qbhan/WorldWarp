def extend_poses_loop(poses, intrinsics, n_new_frames):
    """
    Loop/Tile: Repeat the pose sequence from the beginning.
    Good for cyclical movements like orbiting.
    """
    if n_new_frames <= 0:
        return poses, intrinsics
    
    print(f"🔄 Extending poses by LOOPING ({n_new_frames} new frames)...")
    
    N = poses.shape[1]
    poses_np = poses.cpu().numpy().squeeze(0)
    intrinsics_np = intrinsics.cpu().numpy().squeeze(0)
    
    # Calculate how many times we need to tile
    n_tiles = (n_new_frames // N) + 2
    tiled_poses = np.tile(poses_np, (n_tiles, 1, 1))
    tiled_intrinsics = np.tile(intrinsics_np, (n_tiles, 1, 1))
    
    # Take exactly what we need
    total_needed = N + n_new_frames
    extended_poses = tiled_poses[:total_needed]
    extended_intrinsics = tiled_intrinsics[:total_needed]
    
    return (torch.from_numpy(extended_poses).unsqueeze(0).to(poses.device, dtype=poses.dtype),
            torch.from_numpy(extended_intrinsics).unsqueeze(0).to(intrinsics.device, dtype=intrinsics.dtype))


def extend_poses_pingpong(poses, intrinsics, n_new_frames):
    """
    Ping-Pong/Mirror: Go forward then backward, creating a smooth back-and-forth motion.
    Good for movements that should reverse naturally.
    """
    if n_new_frames <= 0:
        return poses, intrinsics
    
    print(f"🔁 Extending poses by PING-PONG ({n_new_frames} new frames)...")
    
    N = poses.shape[1]
    poses_np = poses.cpu().numpy().squeeze(0)
    intrinsics_np = intrinsics.cpu().numpy().squeeze(0)
    
    # Create ping-pong sequence: forward + reversed (excluding endpoints to avoid duplicates)
    forward = poses_np
    backward = poses_np[-2:0:-1]  # Reverse, excluding first and last
    pingpong = np.concatenate([forward, backward], axis=0)
    
    forward_int = intrinsics_np
    backward_int = intrinsics_np[-2:0:-1]
    pingpong_int = np.concatenate([forward_int, backward_int], axis=0)
    
    # Tile the ping-pong sequence
    pingpong_len = len(pingpong)
    n_tiles = (n_new_frames // pingpong_len) + 2
    tiled_poses = np.tile(pingpong, (n_tiles, 1, 1))
    tiled_intrinsics = np.tile(pingpong_int, (n_tiles, 1, 1))
    
    # Take exactly what we need
    total_needed = N + n_new_frames
    extended_poses = tiled_poses[:total_needed]
    extended_intrinsics = tiled_intrinsics[:total_needed]
    
    return (torch.from_numpy(extended_poses).unsqueeze(0).to(poses.device, dtype=poses.dtype),
            torch.from_numpy(extended_intrinsics).unsqueeze(0).to(intrinsics.device, dtype=intrinsics.dtype))


def extend_poses_extrapolate(poses, intrinsics, n_new_frames, smoothing_window=10):
    """
    Extrapolate: Continue the motion based on recent velocity/angular velocity.
    Good for linear or smoothly curving movements.
    This is the original extrapolate_camera_path function.
    """
    return extrapolate_camera_path(poses, intrinsics, n_new_frames, smoothing_window)


def extend_poses_slowdown(poses, intrinsics, n_new_frames, decay_rate=0.95):
    """
    Slowdown/Decelerate: Continue motion but gradually slow down (never fully stops).
    The decay rate ensures motion continues but gets progressively slower.
    """
    if n_new_frames <= 0:
        return poses, intrinsics
    
    print(f"🐢 Extending poses with SLOWDOWN (decay={decay_rate}, {n_new_frames} new frames)...")
    
    N = poses.shape[1]
    window = min(10, N - 1)
    
    poses_np = poses.cpu().numpy().squeeze(0)
    intrinsics_np = intrinsics.cpu().numpy().squeeze(0)
    
    # Calculate average velocity from last few frames
    start_idx = N - window
    local_lin_vels = []
    local_ang_vels = []
    
    for i in range(start_idx, N - 1):
        P_curr = poses_np[i]
        P_next = poses_np[i+1]
        R_curr, t_curr = P_curr[:3, :3], P_curr[:3, 3]
        R_next, t_next = P_next[:3, :3], P_next[:3, 3]
        global_disp = t_next - t_curr
        local_vel = R_curr.T @ global_disp
        local_lin_vels.append(local_vel)
        R_diff = R_curr.T @ R_next
        rot_vec = R.from_matrix(R_diff).as_rotvec()
        local_ang_vels.append(rot_vec)
    
    avg_lin_vel = np.mean(np.stack(local_lin_vels), axis=0)
    avg_ang_vel = np.mean(np.stack(local_ang_vels), axis=0)
    
    new_poses = []
    last_pose = poses_np[-1]
    curr_R = last_pose[:3, :3]
    curr_t = last_pose[:3, 3]
    
    # Minimum velocity to ensure camera never fully stops
    min_vel_scale = 0.1  # Keep at least 10% of original velocity
    
    for frame_idx in range(n_new_frames):
        # Apply decaying velocity (but never go below minimum)
        decay_factor = max(decay_rate ** frame_idx, min_vel_scale)
        
        effective_lin_vel = avg_lin_vel * decay_factor
        effective_ang_vel = avg_ang_vel * decay_factor
        
        step_global = curr_R @ effective_lin_vel
        curr_t = curr_t + step_global
        
        R_rel_step = R.from_rotvec(effective_ang_vel)
        curr_R_obj = R.from_matrix(curr_R)
        curr_R = (curr_R_obj * R_rel_step).as_matrix()
        
        new_mat = np.eye(4)
        new_mat[:3, :3] = curr_R
        new_mat[:3, 3] = curr_t
        new_poses.append(new_mat)
    
    new_poses = np.stack(new_poses, axis=0)
    full_poses_np = np.concatenate([poses_np, new_poses], axis=0)
    
    last_intrin = intrinsics_np[-1:]
    new_intrinsics = np.repeat(last_intrin, n_new_frames, axis=0)
    full_intrinsics_np = np.concatenate([intrinsics_np, new_intrinsics], axis=0)
    
    return (torch.from_numpy(full_poses_np).unsqueeze(0).to(poses.device, dtype=poses.dtype),
            torch.from_numpy(full_intrinsics_np).unsqueeze(0).to(intrinsics.device, dtype=intrinsics.dtype))


def extend_poses_interpolate_stretch(poses, intrinsics, target_frames):
    """
    Interpolate/Stretch: Stretch or compress the trajectory to fit target frame count.
    Useful when you want to slow down or speed up the entire motion.
    """
    N = poses.shape[1]
    if target_frames == N:
        return poses, intrinsics
    
    print(f"📏 Stretching poses from {N} to {target_frames} frames via interpolation...")
    
    poses_np = poses.cpu().numpy().squeeze(0)
    intrinsics_np = intrinsics.cpu().numpy().squeeze(0)
    
    # Extract positions and rotations
    positions = poses_np[:, :3, 3]  # (N, 3)
    rotations = [R.from_matrix(poses_np[i, :3, :3]) for i in range(N)]
    quats = np.array([r.as_quat() for r in rotations])  # (N, 4)
    
    # Original and target time indices
    t_orig = np.linspace(0, 1, N)
    t_new = np.linspace(0, 1, target_frames)
    
    # Interpolate positions
    interp_pos = interp1d(t_orig, positions, axis=0, kind='cubic')
    new_positions = interp_pos(t_new)
    
    # Interpolate quaternions (simple linear for now, could use slerp)
    interp_quat = interp1d(t_orig, quats, axis=0, kind='linear')
    new_quats = interp_quat(t_new)
    # Normalize quaternions
    new_quats = new_quats / np.linalg.norm(new_quats, axis=1, keepdims=True)
    
    # Reconstruct poses
    new_poses = np.zeros((target_frames, 4, 4))
    for i in range(target_frames):
        new_poses[i, :3, :3] = R.from_quat(new_quats[i]).as_matrix()
        new_poses[i, :3, 3] = new_positions[i]
        new_poses[i, 3, 3] = 1.0
    
    # Interpolate intrinsics (usually just repeat, but interpolate for completeness)
    interp_intr = interp1d(t_orig, intrinsics_np, axis=0, kind='linear')
    new_intrinsics = interp_intr(t_new)
    
    return (torch.from_numpy(new_poses).unsqueeze(0).to(poses.device, dtype=poses.dtype),
            torch.from_numpy(new_intrinsics).unsqueeze(0).to(intrinsics.device, dtype=intrinsics.dtype))


